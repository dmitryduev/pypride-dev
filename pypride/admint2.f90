    !   after 1972 et-utc has only step offsets. st is the array of step times,*
    !   and si is the step sizes (an added second is +1.)                      *
    !--------------------------------------------------------------------------*
    DELTA = 42.184D0
    DO 250 I = 1, NSTEP
        IF(YEAR.GE.ST(I)) DELTA = DELTA + SI(I)
        IF(YEAR.LT.ST(I)) RETURN
    250   CONTINUE
