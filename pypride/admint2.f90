    DIMENSION D(142), TX(39), TY(39), ST(NSTEP), SI(NSTEP)

    ! si gives amount of step, at the times given in st

    DATA SI/27*1./
    DATA ST/1972.5, 1973.0, 1974.0, 1975.0, 1976.0, 1977.0, 1978.0, &
