            0.03, -0.47, 0.98, -0.86, 2.45, 0.22, 0.37, 2.79, 1.20, 3.52, &
            1.17, 2.67, 3.06, 2.66, 2.97, 3.28, 3.31, 3.33, 3.23, 3.60, &
