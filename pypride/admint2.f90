            -0.17, -1.88, -3.43, -4.05, -5.77, -7.06, -7.36, -7.67, -7.64, -7.93, &
            -7.82, -8.35, -7.91, -8.03, -9.14, -8.18, -7.88, -7.62, -7.17, -8.14, &
            -7.59, -7.17, -7.94, -8.23, -7.88, -7.68, -6.94, -6.89, -7.11, -5.87, &
            -5.04, -3.90, -2.87, -0.58, 0.71, 1.80, 3.08, 4.63, 5.86, 7.21, &
